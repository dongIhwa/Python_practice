#엘리스네 동물농장에 있는 동물들은 다음과 같이 울어요!

#양 - “메에에”
#닭 - “꼬끼오”
#개 - “크르릉”

#animal.py 모듈 안에 동물들의 울음소리를 담은 리스트 animals 를 보고 각 동물이 몇 마리가 있는지 변수에 저장하세요.
#sheep 변수에는 양의 울음소리의 횟수를,
#chicken 변수에는 닭의 울음소리의 횟수를,
#dog 변수에는 개의 울음소리의 횟수를 정확하게 저장해야 합니다.

#지시사항
#변수 sheep, chicken, dog를 선언하고, animal 모듈의 animals 리스트 안의 동물 마리수를 각 변수에 저장하세요.

#양의 마릿수는 sheep 변수에, 닭의 마릿수는 chicken 변수에, 개의 마릿수는 dog 변수에 올바르게 저장되어있어야 합니다.